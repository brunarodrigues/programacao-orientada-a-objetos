base = float(input('Digite a base do quadrado:'))
altura = float(input('Digite a altura do quadrado:'))
calculo_da_área_do_quadrado = base*altura
print('Area do quadrado:{:.2f}'.format(calculo_da_área_do_quadrado))
print('O dobro da área do quadrado é igual a:{:.2f}'.format(calculo_da_área_do_quadrado*2))